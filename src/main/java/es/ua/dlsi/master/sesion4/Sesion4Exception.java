package es.ua.dlsi.master.sesion4;

/**
 * @autor drizo
 */
public class Sesion4Exception extends Exception {
    public Sesion4Exception() {
    }

    public Sesion4Exception(String message) {
        super(message);
    }

    public Sesion4Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public Sesion4Exception(Throwable cause) {
        super(cause);
    }

    public Sesion4Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
